package application;

public class ControlCharacter {

	public static void main(String[] args) {
		// 제어 문자(Control Character) : \n 과 \t
		System.out.println("=============================");
		System.out.println("\t탭\t탭 만큼 띄웁니다");
		System.out.println("=============================");
		System.out.println("자바를\n배우고\n있습니다.");
		//위에는 프린트라인, 아래는 프린트
		//프린트라인은 1개가 1개의 줄을 사용
		//프린트는 공백 없이 연달아서 내용을 출력
		System.out.print("이것은");
		System.out.print("줄을");
		System.out.print("안내립니다.");
	}

}
