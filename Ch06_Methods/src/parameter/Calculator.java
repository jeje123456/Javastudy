package parameter;

public class Calculator { // Calculator = 계산기
	int square(int x) {
		return x*x; // 입력된 x의 값을 제곱으로 리턴
	}
	
	int plus(int x, int y) {
		return x + y;
	}
	
	int minus(int x, int y) {
		return x - y;
	}
	
	int multi(int x, int y) {
		return x * y;
	}
	
	int division(int x, int y) {
		return x / y;
	}
	
}
